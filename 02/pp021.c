// Write a program that uses printf to display the following on the screen:
//        *
//       *
//      *
// *   *
//  * *
//   *

// Answer:
#include <stdio.h>

int main(void)
{
    printf("       *\n");
    printf("      *\n");
    printf("     *\n");
    printf("*   *\n");
    printf(" * *\n");
    printf("  *\n");
    return 0;
}
